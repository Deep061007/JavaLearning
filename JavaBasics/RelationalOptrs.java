class RelationalOptrs
{
public static void main(String[] args)
{
int a = 15;
int b = 30;
boolean result;
result = a >= b;
System.out.println("a >= b: " + result);
result = a <= b;
System.out.println(" a <= b: " + result);
result = a == b;
System.out.println("a == b:" + result);
result = a != b;
System.out.println(" a != b: " + result);
}
}
